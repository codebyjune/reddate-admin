async function main() {
  console.log("开始插入模拟合同数据...");

  let count = 0;
  for (const contract of mockContracts) {
    // 先检查是否已存在
    const existing = await prisma.contract.findUnique({
      where: { contractNo: contract.contractNo },
    });

    if (existing) {
      console.log(`⏭️  合同已存在: ${contract.contractNo}`);
    } else {
      await prisma.contract.create({ data: contract });
      count++;
      console.log(`✅ 已创建合同: ${contract.contractNo} - ${contract.partyBName}`);
    }
  }

  console.log(`\n🎉 本次新增 ${count} 条合同数据！`);
}

main()
  .catch((e) => {
    console.error("❌ 插入失败:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
